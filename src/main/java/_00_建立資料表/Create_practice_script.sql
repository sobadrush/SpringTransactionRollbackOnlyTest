DROP TABLE PRODUCT;
DROP TABLE DETAIL;
DROP TABLE REVENUE;

CREATE TABLE `PRODUCT` (
	`ID`	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	`NAME`	TEXT NOT NULL,
	`PRICE`	INTEGER NOT NULL,
	`QUANTITY`	INTEGER NOT NULL,
	`IMG_URL` TEXT NULL
);


CREATE TABLE `REVENUE` (
	`ID`	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	`INCOME`	INTEGER NOT NULL
);


CREATE TABLE `DETAIL` (
	`ID`	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	`CN`	TEXT NOT NULL,
	`P_ID`	INTEGER NOT NULL
);



USE DB_Emp_Dept;

DROP TABLE IF EXISTS EMP_TB;
DROP TABLE IF EXISTS DEPT_TB;

CREATE TABLE DEPT_TB
(
    DEPTNO  VARCHAR(12) PRIMARY KEY NOT NULL,
	DNAME	VARCHAR(14),
	LOC		VARCHAR(13)
);
CREATE TABLE EMP_TB
(
    EMPNO	 VARCHAR(12) PRIMARY KEY NOT NULL,
	ENAME	 VARCHAR(14),
	JOB		 VARCHAR(13),
	HIREDATE DATE,
	DEPTNO VARCHAR(12) NOT NULL FOREIGN KEY REFERENCES DEPT_TB(DEPTNO)
	--Hibernate 做關聯查詢其實可以不用在實體表格設定foreign-key
);

